After you have positioned the bucket, you can indicate your 
confidence in how likely you believe the coin is going to 
land in your bucket.

If the coin hits your bucket, you win 10 points.  But if it's out, 
you lose 10 points.

When you have positioned your bucket, press SPACEBAR to indicate 
your confidence. Use the touchpad to change the number. After you 
have chosen a number, press SPACEBAR again to shoot the coin.
Try to respond as quickly and accurately as possible. 

First you will do one practice block of 20 coins to get used to the task.